let currentLang = localStorage.getItem('boai_lang') || 'en';

function t(key, params) {
  let str = TRANSLATIONS[currentLang]?.[key] || TRANSLATIONS['en']?.[key] || key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      str = str.replace(`{${k}}`, v);
    });
  }
  return str;
}

function setLanguage(lang) {
  if (!TRANSLATIONS[lang]) return;
  currentLang = lang;
  localStorage.setItem('boai_lang', lang);
  // Update all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = t(el.dataset.i18nTitle);
  });
  // Dispatch event for custom handlers
  document.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
}

function renderLanguageSelector() {
  const langs = [
    { code: 'en', label: 'EN', flag: '🇬🇧' },
    { code: 'de', label: 'DE', flag: '🇩🇪' },
    { code: 'ro', label: 'RO', flag: '🇷🇴' },
  ];
  return langs.map(l =>
    `<button onclick="setLanguage('${l.code}')" class="lang-btn px-2 py-1 rounded text-xs font-medium transition-all ${currentLang === l.code ? 'bg-accent/20 text-accent-light' : 'text-dark-300 hover:text-dark-100'}" data-lang="${l.code}">${l.flag} ${l.label}</button>`
  ).join('');
}

// Expose as namespaced API for use by other modules
window.BOAI_I18N = { t, setLanguage, renderLanguageSelector };
